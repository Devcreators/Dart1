// 3강 Numbers
void main() {
  // 자료형
  int number1 = 12; // 정수형
  print(number1);

  int number2 = 38;
  print(number2);

  // 정수 사칙연산
  print(number1 + number2);
  print(number1 - number2);
  print(number1 * number2);
  print(number1 / number2);

  print('-------------');

  double number3 = 2.5; // 실수
  double number4 = 0.5;

  print(number3);
  print(number4);

  // 실수 사칙연산
  print(number3 + number4);
  print(number3 - number4);
  print(number3 * number4);
  print(number3 / number4);
}
